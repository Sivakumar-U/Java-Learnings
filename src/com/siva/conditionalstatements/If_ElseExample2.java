package com.siva.conditionalstatements;

/*
Check the number is Even or Not
Note: Braces({}) not required if block has only single statement.
*/

public class If_ElseExample2 {

	public static void main(String[] args) {
		
		int num = 8;
		if(num % 2 == 0) 
			System.out.println(num+ " is Even number");
		else
			System.out.println(num+ " is not Even number");
	
	}

}
