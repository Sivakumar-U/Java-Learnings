// Check the biggest of two numbers.
public class BiggestOf2Nums_UsingIf_Else {

	public static void main(String[] args) {
		
		int a = 10, b = 20;
		if(a > b)
            System.out.println("a is big");
        else
            System.out.println("b is big");

	}

}
